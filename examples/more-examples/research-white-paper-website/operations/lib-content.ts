import { readdir, stat, readFile } from "fs/promises";
import path from "path";

export const ROOT = process.cwd();

export const SECTIONS: { slug: string; label: string; blurb: string }[] = [
  { slug: "sources", label: "Sources", blurb: "Primary texts and PDFs — Old English Herbarium (Van Arsdall translation) and Review of Scholarship." },
  { slug: "analysis", label: "Analysis", blurb: "Research findings — candidate remedies, expanded candidates, deep-research notes, theoretical framework, paper arcs." },
  { slug: "drafts", label: "Drafts", blurb: "Working paper drafts — ethnopharmacology, intellectual history, folklore-ritual." },
  { slug: "proposal", label: "Proposal", blurb: "Experimental proposal." },
  { slug: "agent-output", label: "Agent output", blurb: "Drafts, syntheses, and bibliographies generated by AI agents." },
];

export type FileEntry = {
  name: string;
  parts: string[];
  isDirectory: boolean;
  size?: number;
  ext: string;
};

const SAFE_PREFIXES = SECTIONS.map((s) => s.slug);

function ensureSafe(parts: string[]): string {
  if (parts.length === 0) throw new Error("empty path");
  if (!SAFE_PREFIXES.includes(parts[0])) throw new Error("forbidden");
  const full = path.normalize(path.join(ROOT, ...parts));
  const sectionRoot = path.join(ROOT, parts[0]);
  if (!full.startsWith(sectionRoot)) throw new Error("traversal");
  return full;
}

export async function listDir(parts: string[]): Promise<FileEntry[]> {
  const full = ensureSafe(parts);
  const entries = await readdir(full, { withFileTypes: true });
  const out: FileEntry[] = [];
  for (const e of entries) {
    if (e.name.startsWith(".")) continue;
    const ePath = [...parts, e.name];
    const st = e.isFile() ? await stat(path.join(full, e.name)) : undefined;
    out.push({
      name: e.name,
      parts: ePath,
      isDirectory: e.isDirectory(),
      size: st?.size,
      ext: path.extname(e.name).toLowerCase(),
    });
  }
  out.sort((a, b) => {
    if (a.isDirectory !== b.isDirectory) return a.isDirectory ? -1 : 1;
    return a.name.localeCompare(b.name);
  });
  return out;
}

export async function readText(parts: string[]): Promise<string> {
  const full = ensureSafe(parts);
  return readFile(full, "utf8");
}

export async function statPath(parts: string[]) {
  const full = ensureSafe(parts);
  return stat(full);
}

export async function readBinary(parts: string[]): Promise<Buffer> {
  const full = ensureSafe(parts);
  return readFile(full);
}

export function humanSize(bytes?: number): string {
  if (bytes === undefined) return "";
  if (bytes < 1024) return `${bytes} B`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
  return `${(bytes / 1024 / 1024).toFixed(1)} MB`;
}

export function extKind(ext: string): "markdown" | "text" | "pdf" | "code" | "data" | "other" {
  if (ext === ".md") return "markdown";
  if (ext === ".pdf") return "pdf";
  if (ext === ".txt" || ext === ".wiki") return "text";
  if (ext === ".py" || ext === ".ts" || ext === ".tsx" || ext === ".js") return "code";
  if (ext === ".json") return "data";
  return "other";
}

export function readmePath(): string {
  return path.join(ROOT, "README.md");
}

export async function readReadme(): Promise<string> {
  return readFile(readmePath(), "utf8");
}
